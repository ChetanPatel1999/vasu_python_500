#display data using formate specifier
# %d --> integer value
# %f --> float value
# %c --> character value
# %s --> string value
a=120
b=50
c=a+b
name="raj varma"
cm=5.7
ch=1
print("value of a = %d"%a)
print("value of b = %d"%b)
print("sum of %d and %d = %d"%(a,b,c))
print("distance = %.2f cm"%cm)
print("distance = %.1f cm"%cm)
print("person name = %s"%name)
print("value of ch = %c"%ch)






